"""
Phase 1: Boltzmann Equation Derivation for HPM
================================================

This module derives the phase correlation parameter η(ℓ) from first principles,
starting from the Thomson scattering Boltzmann equations.

Key Physics:
- Thomson scattering creates polarization from temperature anisotropies
- The photon-baryon fluid dynamics at recombination imprint phase correlations
- η(ℓ) quantifies the phase coherence between temperature and polarization modes
"""

import numpy as np
from scipy.special import spherical_jn
from scipy.integrate import quad


class ThomsonScatteringBoltzmann:
    """
    Boltzmann equation solver for Thomson scattering polarization generation.
    
    Derives phase correlation from first principles following:
    1. Radiation transfer equation for polarized light
    2. Photon-baryon fluid coupling
    3. Visibility function formalism
    """
    
    def __init__(self, cosmology):
        """
        Initialize with cosmological parameters.
        
        Parameters
        ----------
        cosmology : dict
            Contains: omega_b, omega_c, H0, tau_reion, z_dec
        """
        self.omega_b = cosmology['omega_b']
        self.omega_c = cosmology['omega_c']
        self.H0 = cosmology['H0']
        self.tau_reion = cosmology['tau_reion']
        self.z_dec = cosmology['z_dec']
        
        # Thomson cross section (m^2)
        self.sigma_T = 6.6524587158e-29
        
        # Calculate derived quantities
        self._compute_recombination_physics()
        
    def _compute_recombination_physics(self):
        """Compute key recombination quantities."""
        # Speed of sound in baryon-photon fluid at recombination
        # c_s^2 = 1/(3(1+R)) where R = 3ρ_b/(4ρ_γ)
        R_dec = 31.5 * self.omega_b * (2.7/2.726)**4 * (self.z_dec/10**3)**(-1)
        self.c_s = 1/np.sqrt(3*(1+R_dec))
        
        # Silk damping scale (approximate)
        # k_D^{-2} = (1/2)∫ dη (1/(1+R)) (1/(τ̇ a^2))
        # Approximate value for ΛCDM
        self.k_D = 0.14  # Mpc^-1 at z ≈ 1100
        
        # Sound horizon at recombination
        self.r_s = 150.0  # Mpc (approximate)
        
        # Optical depth to recombination
        self.tau_dec = 0.088  # approximate
        
        # Damping scale in multipole space
        self.ell_damp = self.k_D * 14000  # Rough conversion
        
    def visibility_function(self, z):
        """
        Compute visibility function g(z) = -τ̇ exp(-τ).
        
        Using standard recombination treatment.
        """
        # Simplified Peebles model
        # τ ≈ 0.37 (z/1000)^16.25
        tau = 0.37 * (z/1000)**16.25
        
        # Derivative: dτ/dz
        dtau_dz = 0.37 * 16.25 * (z/1000)**15.25 / 1000
        
        # Visibility function
        g = -dtau_dz * np.exp(-tau)
        return g
    
    def source_temperature(self, k, eta):
        """
        Temperature source function S_T(k,η) for Boltzmann equation.
        
        S_T = g(η)[Θ_0 + Ψ - i(k·v_b)/k] + exp(-τ)[Θ̇_0 + Ψ̇]
        
        where:
        - Θ_0 is monopole (temperature fluctuation)
        - Ψ is gravitational potential
        - v_b is baryon velocity
        - g is visibility function
        """
        # Simplified: use oscillating solution for photon-baryon fluid
        # Θ_0(k,η) = A(k) cos(k r_s) exp(-(k/k_D)^2)
        
        omega = k * self.c_s  # Oscillation frequency
        damping = np.exp(-(k/self.k_D)**2)
        
        # Temperature monopole
        Theta_0 = np.cos(k * self.r_s) * damping
        
        # Velocity term (out of phase by π/2)
        v_b = self.c_s * np.sin(k * self.r_s) * damping
        
        return Theta_0, v_b
    
    def source_polarization(self, k, eta):
        """
        Polarization source function for E-modes.
        
        From Ma & Bertschinger (1995):
        S_P^(2) = -g(η) [3/4 Θ_2(1 - ∂_η²/η²)]
        
        The quadrupole Θ_2 generates E-mode polarization.
        """
        # Quadrupole source (peaks where temperature gradient is maximum)
        # Θ_2 ∝ k v_b / τ̇ (diffusion approximation)
        
        _, v_b = self.source_temperature(k, eta)
        
        # Polarization quadrupole is generated by Thomson scattering
        # of temperature quadrupole
        # Θ_2 ≈ (8k/15τ̇) v_b in diffusion limit
        tau_dot = 1/self.tau_dec  # Rough estimate
        Theta_2 = (8*k/(15*tau_dot)) * v_b
        
        return Theta_2
    
    def compute_phase_correlation(self, ell):
        """
        Compute phase correlation coefficient η(ℓ).
        
        The phase correlation emerges because:
        1. Temperature and polarization share the same source (Thomson scattering)
        2. They propagate through the same gravitational potential
        3. But polarization has additional geometric projection
        
        η(ℓ) = ⟨arg(C_TE)⟩ / (π/4)
        
        In Thomson scattering, C_TE acquires a characteristic phase
        because E-modes are generated from temperature quadrupoles
        which have a specific phase relationship to the monopole.
        """
        # Convert multipole to wavenumber
        # ℓ ≈ k χ_rec where χ_rec is comoving distance to recombination
        chi_rec = 14000  # Mpc (approximate comoving distance to z=1100)
        k = ell / chi_rec
        
        # Phase of temperature (monopole dominates)
        phi_T = np.angle(np.exp(1j * k * self.r_s))
        
        # Phase of E-mode polarization
        # Generated from temperature quadrupole which has phase shift
        # relative to monopole due to streaming motion
        phi_E = phi_T + np.pi/4  # Characteristic π/4 phase shift
        
        # Phase correlation: how aligned are T and E phases?
        # In Thomson scattering, they should be correlated with shift
        delta_phi = phi_E - phi_T
        
        # Normalized phase correlation
        # η = 1 means perfect correlation, 0 means no correlation
        # For Thomson scattering, expect η ~ 0.5 (sinusoidal relationship)
        eta = np.sin(2*delta_phi) / 2
        
        return eta
    
    def derive_eta_parameters(self):
        """
        Derive the HPM parameters η₀, α_η from physical quantities.
        
        Connection to physical parameters:
        - η₀: baseline phase correlation from Thomson scattering geometry
        - α_η: scale dependence from Silk damping and recombination physics
        - ℓ_*: transition scale from correlation to decorrelation
        """
        # Baseline phase correlation
        # From Thomson scattering geometry: 2nd-order Legendre polynomial
        # η₀ ≈ sqrt(3)/4 ≈ 0.433 for pure Thomson scattering
        self.eta_0 = np.sqrt(3)/4
        
        # Scale dependence from damping physics
        # α_η ∝ k_D^(-2) where k_D is Silk damping scale
        # Also depends on baryon density
        self.alpha_eta = 1.0 / (self.k_D * self.r_s)
        
        # Transition scale
        # ℓ_* ≈ k_D × χ_rec (damping scale in multipole space)
        self.ell_star = self.ell_damp
        
        return {
            'eta_0': self.eta_0,
            'alpha_eta': self.alpha_eta,
            'ell_star': self.ell_star,
            'k_D': self.k_D,
            'r_s': self.r_s,
            'c_s': self.c_s
        }


class PhaseHierarchyCalculator:
    """
    Calculate the hierarchy ratio R_H from phase correlations.
    
    R_H = 4η² is the key HPM prediction.
    """
    
    def __init__(self, eta_0=0.433, alpha_eta=0.5, ell_star=1000):
        self.eta_0 = eta_0
        self.alpha_eta = alpha_eta
        self.ell_star = ell_star
        
    def eta_of_ell(self, ell):
        """
        Phase correlation as function of scale.
        
        η(ℓ) = η₀ (ℓ/ℓ_*)^(-α_η) for ℓ < ℓ_*
        η(ℓ) → 0 for ℓ > ℓ_* (Silk damping destroys correlation)
        """
        eta = self.eta_0 * (ell / self.ell_star)**(-self.alpha_eta)
        eta[ell > self.ell_star] = 0
        return eta
    
    def hierarchy_ratio(self, ell):
        """
        Calculate R_H(ℓ) = C_TE² / (C_TT × C_EE).
        
        In HPM: R_H = 4η² + (1-4η²) × R_H,ΛCDM
        
        For small η: R_H ≈ 4η² when R_H,ΛCDM ≈ 0
        """
        eta = self.eta_of_ell(ell)
        return 4 * eta**2


